//program to find area of rectangale (input is from user)
#include<stdio.h>
int main()
{
    int length,breadth;
    printf("the length of the rectangle\n");
    scanf("%d",&length);
    printf("the breadth of the rectangle\n");
    scanf("%d",&breadth);
    printf("the area of rectangle is %d",length*breadth);
    return 0;
}